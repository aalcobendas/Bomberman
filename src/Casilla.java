
public class Casilla {
	
	//ATRIBUTOS
	protected int tipo;
	protected boolean romper;
	protected boolean pasar;
	
	//METODOS
	public int getTipo() {
		return tipo;
	}
	public void setTipo(int tipo) {
		this.tipo = tipo;
	}
	public boolean isRomper() {
		return romper;
	}
	public void setRomper(boolean romper) {
		this.romper = romper;
	}
	public boolean isPasar() {
		return pasar;
	}
	public void setPasar(boolean pasar) {
		this.pasar = pasar;
	}
}
